package javaPractice;

public class Practice13Even {

	
	public static void main(String[] args) {
		
				
			for (int t=0; t<=100; t++) {
				if(t%2==0) {
					System.out.println(t +"The number is even");
					System.out.println();
				}else {
					System.out.println(t +"The number is odd");
				}
			}
	}
}
