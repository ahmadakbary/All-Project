package javaPractice;

public class Practice10NestedIfStatement {
public static void main(String[] args) {
	
	if (2>3) {
		System.out.println("Yes 2 is bigger than 3");
	}else {
		System.out.println("No 3 is bigger than 2");
		
	if (4>6) {
		System.out.println("Yes 4 is bigger than 6");
	}else {
		System.out.println("No 6 is bigger than 4");
	}
	
	int b = 5;
	if (b>3) {
		System.out.println("Yes 5 is smaller than 3");
		
	}else {
		System.out.println("Yes 5 is bigger");
	}
	}
}
}
