package BaseJava;
public class MethodStatic {
public static void main(String[] args) {	
forloop();	
Array();
}
	public static void forloop() {
		for (int i = 0; i<50; i++) {
		System.out.println(i);
	}
		System.out.println();
		System.out.println("array");
	}
		
	
		public static void Array() {
			int [] data = new int [3];
			data [0] = 2;
			data [1] = 5;
		for (int i = 0; i<data.length; i++) {
			System.out.println(i);
			
			
			
			}
		
	}
}

