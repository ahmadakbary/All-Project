package InterviewQuestions;
public class I_Q_forLoopAccendingDescending{
public static void main(String[] args) {
	
	
	
	
	// Acceding numbers
	for (int i = 0; i<=100; i++) {
		System.out.println(i);
		
	//Descending number
	for (int j = 100; j>=0; j--)
		System.out.println(" "+j);
	
	//print from 10 to 17
	for (int a = 10; a<=17; a++) {
		System.out.println(a);
	}
	}
		}
}	

	


