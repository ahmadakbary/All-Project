package Day4;
public class TwoDimensionalDynamicArray {
public static void main(String[] args) {
		
int a [][]= { {100,200}, {300,400}, {500,600} };
System.out.println(a.length);// Represent 3Row
System.out.println(a[0].length); // Represent 3Col & 2Rows
System.out.println(a[2][1]);//Specify the Rows & col

	
	}
}
