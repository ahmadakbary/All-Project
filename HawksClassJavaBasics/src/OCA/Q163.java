package OCA;

public class Q163 {
public static void main(String[] args) {
	
	int i = 0;
	int j = 7;
	
	for (i = 0; i<j; i=i + 2) {
		System.out.print(i +" ");

		
		/*
	i = 0 → 0 < 7 → true → print 0 → i = 0 + 2 = 2

	i = 2 → 2 < 7 → true → print 2 → i = 2 + 2 = 4

	i = 4 → 4 < 7 → true → print 4 → i = 4 + 2 = 6

	i = 6 → 6 < 7 → true → print 6 → i = 6 + 2 = 8

	i = 8 → 8 < 7 → false → exit loop

	Output:
	0 2 4 6
		 */
}
}
}