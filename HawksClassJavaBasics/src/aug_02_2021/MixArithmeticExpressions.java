package aug_02_2021;

public class MixArithmeticExpressions {

	public static void main(String[] args) {
		
		
		double x = 10.5;
		double y = 13.34;
		
		double sum = x+y;
		System.out.println("sum is the total of x and y: "+sum);
		double multp = x*y;
		System.out.println("sum is the total of x and y: "+multp);
		double div = x=y;
		System.out.println("sum is the total of x and y: "+div);
	}
}
