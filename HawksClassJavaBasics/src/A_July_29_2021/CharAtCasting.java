package A_July_29_2021;

public class CharAtCasting {
public static void main(String[] args) {
	
	int c = 'a';
	int d = 'b';
	System.out.println(c>d);
	System.out.println((int)c);
	
	int a = 'd';
	int b = 'e';
	System.out.println(a);
	System.out.println(a>b);
	System.out.println((int)a);
	
	
	
	
	
	
	
	
}
}
