package A_July_22_2021;

public class Substring {
	public static void main(String[] args) {
		
		String strName = "Ahmad Shekaib Akbary";
		String SubstringOfName = strName.substring(2);
		System.out.println("My substring is: "+SubstringOfName);
		
		
		
		String str1Name = "ahmad shekaib akbary";
		String substringOfName = str1Name.substring(4,12);
		System.out.println("My substring is: "+substringOfName);
	}

}
