package A_Aug_04_2021;

public class WhileLoop {
public static void main(String[] args) {
	
	int a = 1;
	while (a<=10) {
		System.out.println(a++);
	}
	
	
	
	System.out.println("*****************************");
	int b = 1;
	while (b<=10) {
		System.out.println(b);
		b +=2;
	}
	System.out.println("*******************************");
	int d = 1;
	while (d<=1) {
		System.out.println(d++ +"\t");
	}
	
}
}
