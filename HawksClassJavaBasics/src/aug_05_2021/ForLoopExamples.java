package aug_05_2021;
public class ForLoopExamples {
public static void main(String[] args) {
				
//print from 0 to 10
for (int i=0; i<=10; i++) {
System.out.println(i);
			
}	
System.out.println("----------");
// reverse number from 10 to 0
for (int i=10; i>=0; i--) {
System.out.println(" "+i);
}

System.out.println("-----------");
for (int i=10; i<=17; i++) {
System.out.println(i);
	
	}
	
	
}
}