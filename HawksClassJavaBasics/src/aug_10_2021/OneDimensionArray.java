package aug_10_2021;
public class OneDimensionArray {
public static void main(String[] args) {

	String  b [] = { "SD", "AK", "CA"};
	
	for (int row=0; row <b.length; row++) {
	for (int col=0; col <b[row].length(); col++) {
		System.out.println(b[row] + " ");
		}
		System.out.println();
	
	
	
	
	
	
	
	
	
	}

}
}